// ── 正規化 ────────────────────────────────────────────────────────────────
// F軸: R(発信型) / A(吸収型)
// A軸: L(情熱型) / G(秘愛型)
// C軸: O(開放型) / S(選縁型)
// D軸: H(直感型) / M(意志型)
function normalizeCode(raw: string): string {
  const upper = raw.toUpperCase().padEnd(4, 'R')
  const [f, a, c, d] = upper.split('')
  const normF = f === 'A' ? 'A' : 'R'
  const normA = a === 'G' ? 'G' : 'L'
  const normC = c === 'S' ? 'S' : 'O'
  const normD = d === 'M' ? 'M' : 'H'
  return `${normF}${normA}${normC}${normD}`
}

// ── ページ ────────────────────────────────────────────────────────────────
export default async function ResultPage({
  params,
}: {
  params: Promise<{ code: string }>
}) {
  const { code } = await params
  const normalizedCode = normalizeCode(code)
  const typeInfo = TYPES[normalizedCode]

  if (!typeInfo) notFound()

  const displayCode = normalizedCode
  const characterImage = `/${typeInfo.name}.png`

  const celebrities = typeInfo.celebrities.map((name) => ({
    name,
    description: '',
  }))

  return (
    <main className="min-h-screen bg-[#FFF8F5]">
      <Header />

      <HeroSection
        code={displayCode}
        typeName={typeInfo.name}
        catchphrase={typeInfo.tagline}
        characterImage={characterImage}
      />

      <PersonalitySection paragraphs={typeInfo.descriptions} />

      <DiagnosisBasisSection code={normalizedCode} />

      <FamousExamplesSection celebrities={celebrities} />

      {/* 無料コンテンツ: 相性一覧 */}
      <section className="py-8">
        <CompatibilitySection typeCode={normalizedCode} />
      </section>

      {/* 無料コンテンツ: 恋愛傾向 + 仕事運・財産運（Claude生成・キャッシュ付き） */}
      <FreeGeneratedContent code={normalizedCode} />

      {/* 国別統計 */}
      <CountryStatsSection currentTypeCode={normalizedCode} />

      {/* <CommunityCommentsSection typeName={typeInfo.name} typeCode={normalizedCode} /> */}

      <UpgradeCta code={normalizedCode} />

      <ShareSection
        code={displayCode}
        typeName={typeInfo.name}
      />

      <Footer />

      {/* 国選択モーダル（ログイン済み・未設定の場合のみ表示） */}
      <CountrySetup typeCode={normalizedCode} />
    </main>
  )
}
